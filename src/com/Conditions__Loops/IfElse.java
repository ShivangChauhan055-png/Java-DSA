package com.Conditions__Loops;

public class IfElse {
    public static void main(String[] args) {
        int number = 70;
        if(number >=80){
            System.out.println("A grade");
        } else if (number>=60) {
            System.out.println("Pass");
        }else{
            System.out.println("Fail");
        }
    }
}
